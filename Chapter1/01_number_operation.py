#계산에 하나라도 소수형이 들어 있으면 결과 값은 소수형으로 나온다.

#나눗셈 >> 항상 소수형으로 나온다
print(7/2)
print(6/2)
print(7.0/2)

#혼합형 >> 파이썬도 일반적인 사칙연산의 법칙을 따른다
